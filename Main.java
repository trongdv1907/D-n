public class Main {
    public static void main(String[] args) {
        // Khai báo và gán giá trị cho biến soA và soB
        int soA = 10;
        int soB = 30;
        
        // Tính tổng của soA và soB
        int tongHaiSo = soA + soB;
        
        // Hiển thị kết quả ra màn hình
        System.out.println("soA = " + soA);
        System.out.println("soB = " + soB);
        System.out.println("Tong hai so = " + tongHaiSo);
    }
}

